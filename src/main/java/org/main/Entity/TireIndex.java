package org.main.Entity;

public class TireIndex {
    String name;
    String vlaue;

    public TireIndex(String name, String vlaue) {
        this.name = name;

        this.vlaue = vlaue;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVlaue() {
        return vlaue;
    }

    public void setVlaue(String vlaue) {
        this.vlaue = vlaue;
    }
}
